void send_adc_data(uint32_t *data, uint32_t length)
{

    // Clean Cache so USB sees the RAM data
    SCB_CleanDCache_by_Addr((uint32_t*)data, length * 4);
    // length is in words (32-bit), USB needs bytes, so length * 4
    CDC_Transmit_HS((uint8_t*)data, length * 4);
}
