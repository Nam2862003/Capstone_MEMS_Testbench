uint8_t CDC_Transmit_HS(uint8_t* Buf, uint16_t Len);  // forward declare

void send_adc_data(uint8_t* data, uint32_t len)
{
    uint32_t offset = 0;

    while (offset < len)
    {
        uint16_t chunk = (len - offset > 512) ? 512 : (len - offset);

        while (CDC_Transmit_HS(data + offset, chunk) == USBD_BUSY);

        offset += chunk;
    }
}

